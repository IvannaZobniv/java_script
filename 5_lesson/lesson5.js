// - створити функцію яка повертає найменьше число з масиву
const funMin = (array) => {
    let min = array[0];
    for (const funMinElement of array) {
        if (funMinElement < min) min = funMinElement;
        }
    return min;
}
console.log(funMin([2, 3, 4, -5, 6, 10, 4, -3, 6]));
