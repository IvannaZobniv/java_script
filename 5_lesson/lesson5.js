// - створити функцію яка приймає масив чисел, сумує значення елементів масиву та повертає його.
// Приклад [1,2,10]->13
function arr(arr){
    let sum = 0;
    for (const item of arr){
        sum+=item;
    }
    return sum;
}
console.log(arr([1,2,10]));